color = ["r", "o", "y", "g", "b", "i", "v"]
r = "red"
o = "orange"
y = "yellow"
g = "green"
b = "blue"
i = "indigo"
v = "violet"

def display_rainbow(color)
  puts "#{color[r]}, #{color[o]}, #{color[y]}, #{color[g]}, #{color[b]}, #{color[i]}, #{color[v]}"
end

display_rainbow("color")
